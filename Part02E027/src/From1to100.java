
public class From1to100 {
	
	
	
	public static void main (String[] args) {
		System.out.println("Exercise E027; From 1 to 100");
		
		int i = 0;
		
		while(i<100) {
			i = i + 1;
			System.out.print (i + "  ");
			
		}
		System.out.println(" ");
		System.out.println("Finish! Part1");
		
		System.out.println("Exercise E028: From 100 to 1");
		
		int j = 100;
		while(j>0) {
			System.out.print (j + "  ");
			j = j - 1;
		}
		
		System.out.println(" ");
		System.out.println("Finish! Part2a");
	} 
	
		

}
